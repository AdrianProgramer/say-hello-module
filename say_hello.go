package module_go

func SayHello(name string) string {
	return name
}

func KalkulatorTambah(number1 int, number2 int, number3 int) int {
	return number1 + number2 + number3
}

func KalkulatorKurang(number1 int, number2 int, number3 int) int {
	return number1 - number2 - number3
}

func KalkulatorBagi(number1 int, number2 int, number3 int) int {
	return number1 / number2 / number3
}

func Modulus(number1 int, number2 int, number3 int) int {
	return number1 % number2
}
